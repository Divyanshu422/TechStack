
/*
    * Function which is mostly used in the Coding world:
    *  1. floor: rounds down the number to the nearest integer
    *  2. ceil: rounds up the number to the nearest integer
    *  3. Random: returns a floating-point, pseudo-random 
    *             number between 0 (inclusive) and 1 (exclusive).
*/

// Usage
// In rock-paper and sessior - will need to use the Random function

console.log(Math.floor(3.999999))   //Output: 3
console.log(Math.floor(-4.7)); // Output: -5
console.log(Math.ceil(4.3));  // Output: 5
console.log(Math.ceil(-4.7)); // Output: -4
console.log(Math.random()); // Output: A random number between 0 and 1, e.g., 0.234567